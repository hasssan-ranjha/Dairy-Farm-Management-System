package FarmManagement;

import java.time.LocalDate;

public abstract class Animal {
    protected String id;
    protected AnimalStatus status;
    protected LocalDate entryDate;

    public Animal(String id, LocalDate entryDate) {
        this.id = id;
        this.entryDate = entryDate;
        this.status = AnimalStatus.ACTIVE;
    }

    public String getId() {
        return id;
    }

    public AnimalStatus getStatus() {
        return status;
    }

    public LocalDate getEntryDate() {
        return entryDate;
    }
}
